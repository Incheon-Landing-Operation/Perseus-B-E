package com.example.perseus.domain.factUpdate.entity.type;

public enum CognitiveDistortion {
  DICHOTOMOUS_THINKING, OVERGENERALIZATION,
  FILTERING, JUMPING_TO_CONCLUSIONS,
  MIND_READING, CATASTROPHIZING, EMOTIONAL_REASONING,
  SHOULD_STATEMENTS, SELF_BLAMING, BIASES, BLAMING_OTHERS
}

// DICHOTOMOUS THINKING – 이분법적 사고
// OVERGENERALIZATION – 과잉 일반화
// FILTERING – 필터링 (부정적 정보만 선택적으로 받아들이기)
// JUMPING TO CONCLUSIONS – 성급한 결론
// MIND READING – 예단 (상대 마음을 안다고 생각하기)
// CATASTROPHIZING – 부정적인 생각 극대화 (파국화)
// EMOTIONAL REASONING – 감정적 추론
// SHOULD STATEMENTS – 당위적 명령
// SELF-BLAMING – 자기 비난
// BIASES – 편견
// BLAMING OTHERS – 타인 비난